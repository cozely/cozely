//------------------------------------------------------------------------------

func TestSqrt(t *testing.T) {
	var x float32
	for _, tt := range sqrtTests {
		x = Sqrt(tt.in)
		if x != tt.out {
			t.Errorf("Error for Sqrt(%.100g): %.100g instead of %.100g\n", tt.in, x, tt.out)
		}
	}
}

