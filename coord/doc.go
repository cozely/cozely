/*
Package coord provides vector types for various coordinates systems.

  - XY for 2D cartesian coordinates
  - XYZ for 3D cartesian cordinates (or 2D projective coordinates)
  - XYZW for 3D projective coordinates

  - RA for polar coordinates
  - RAD for cylindrical coordinates
  - RAP for spherical coordinates

  - CR for 2D cartesian grid coordinates (e.g. pixel coordinates)
  - QR for hexagonal grid coordinates
  - XL for hexagonal axial coordinates (i.e. like QR but floating point)
  - QRT for triangular grid coordinates
*/
package coord
